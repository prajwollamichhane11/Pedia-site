Pediatricians are doctors who manage the health of your child, including physical,
behavior, and mental health issues. They're trained to diagnose and treat childhood
illnesses, from minor health problems to serious diseases.

Pediatricians have an education that gives them special skills to take care of
 your child's health. They graduated from medical school and completed a 3-year residency
  program in pediatrics.

You'll want to find one who's also "board-certified." That means they've passed rigorous
