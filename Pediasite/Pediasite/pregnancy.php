    <h3>First Trimester  (week 1–week 12) </h3>
    <p>
     During the first trimester your body undergoes many changes.
                    Hormonal changes affect almost every organ system in your body.
                    These changes can trigger symptoms even in the very first weeks of pregnancy.
                    Your period stopping is a clear sign that you are pregnant.As your body changes,
                    you might need to make changes to your daily routine,
                    such as going to bed earlier or eating frequent, small meals. Fortunately,
                    most of these discomforts will go away as your pregnancy progresses.
                    And some women might not feel any discomfort at all! If you have been pregnant before,
                    you might feel differently this time around. Just as each woman is different,
                    so is each pregnancy.
                    Other changes may include:
                    <ol type="1">
                      <li>Extreme tiredness</li>
                      <li>Tender, swollen breasts. Your nipples might also stick out.</li>
                      <li>Upset stomach with or without throwing up (morning sickness)</li>
                      <li>Cravings or distaste for certain foods</li>
                      <li>Mood swings</li>
                      <li>Constipation (trouble having bowel movements)</li>
                      <li>Need to pass urine more often</li>
                      <li>Headache</li>
                      <li>Heartburn</li>
                      <li>Weight gain or loss</li>
                    </ol>

      </p>
    <h3>Second Trimester  (week 13–week 28) </h3>
    <p>
      Most women find the second trimester of pregnancy easier than the first.But it is just as important to stay informed about your pregnancy during these months.You might notice that symptoms like nausea and fatigue are going away.But other new, more noticeable changes to your body are now happening.Your abdomen will expand as the baby continues to grow.And before this trimester is over, you will feel your baby beginning to move!
    </p>

      As your body changes to make room for your growing baby, you may have:
<ol type="1">

    <li>Body aches, such as back, abdomen, groin, or thigh pain</li>
    <li>Stretch marks on your abdomen, breasts, thighs, or buttocks</li>
    <li>Darkening of the skin around your nipples</li>
    <li>A line on the skin running from belly button to pubic hairline</li>
    <li>Patches of darker skin, usually over the cheeks, forehead, nose, or upper lip.</li>
    <li>Patches often match on both sides of the face.</li>
    <li>This is sometimes called the mask of pregnancy.</li>
    <li>Numb or tingling hands, called carpal tunnel syndrome</li>
    <li>Itching on the abdomen, palms, and soles of the feet.</li>
  </ol>
    (Call your doctor if you have nausea, loss of appetite, vomiting, jaundice or fatigue combined with itching.
    These can be signs of a serious liver problem.)
    Swelling of the ankles, fingers, and face.
(If you notice any sudden or extreme swelling or if you gain a lot of weight really quickly, call your doctor right away.
This could be a sign of preeclampsia.)
