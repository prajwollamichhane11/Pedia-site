                          <h3>Third Trimester  (week 29–week 40) </h3>
                      <p>
                        You're in the home stretch! Some of the same discomforts you had in your second trimester will continue.
                          Plus, many women find breathing difficult and notice they have to go to the bathroom even more often.
                          This is because the baby is getting bigger and it is putting more pressure on your organs.
                          Don't worry, your baby is fine and these problems will lessen once you give birth.
                          <p>
                              Some new body changes you might notice in the third trimester include:
                          </p>
                          <ol type="1">
                            <li>Heartburn</li>
    <li>Swelling of the ankles, fingers, and face. (If you notice any sudden or extreme swelling or if you gain a lot of weight really quickly,
    call your doctor right away.
    This could be a sign of preeclampsia.)</li>
    <li>Hemorrhoids</li>
    <li>Tender breasts, which may leak a watery pre-milk called colostrum (kuh-LOSS-struhm)</li>
    <li>Your belly button may stick out</li>
    <li>Trouble sleeping</li>
    <li>The baby "dropping", or moving lower in your abdomen</li>
    <li>Contractions, which can be a sign of real or false labor</li>
  </ol>
    As you near your due date, your cervix becomes thinner and softer (called effacing).
    This is a normal, natural process that helps the birth canal (vagina) to open during the birthing process.
    Your doctor will check your progress with a vaginal exam as you near your due date.
