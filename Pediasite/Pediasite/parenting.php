			                        <h1> Introduction </h1>
                        <p>
                             Parenting or child rearing is the process of promoting and supporting the physical,
                             emotional, social, and intellectual development of a child from infancy to adulthood.
                             Parenting refers to the aspects of raising a child aside from the biological relationship.
                             The most common caretaker in parenting is the biological parent(s) of the child in question,
                             although others may be an older sibling, a grandparent,
                             a legal guardian, aunt, uncle or other family member, or a family friend.
                             Governments and society may have a role in child-rearing as well.
                             In many cases, orphaned or abandoned children receive parental care from non-parent blood relations.
                             Others may be adopted, raised in foster care, or placed in an orphanage. Parenting skills vary,
                             and a parent with good parenting skills may be referred to as a good parent.
                              Good parenting helps foster empathy, honesty, self-reliance, self-control, kindness,
                              cooperation, and cheerfulness, says Steinberg. It also promotes intellectual curiosity,
                              motivation, and desire to achieve. It helps protect children from developing anxiety,
                              depression, eating disorders, anti-social behavior, and alcohol and drug abuse.
                        </p>


