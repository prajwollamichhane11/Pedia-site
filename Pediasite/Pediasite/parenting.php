                            <h3> Act boldly as a</h3>
                             <ol type="1">
                                
                                    <li><b>Role model</b> — Make sure to eat healthy, wholesome foods
                                    and get plenty of exercise yourself. Be consistent; this should be a permanent part
                                    of your lifestyle. Set a good example.</li>

                                    <li><b>Gatekeeper</b>— you have the power to monitor and control what your kids eat,
                                    what exercise they get, and how much time they spend watching TV, surfing the web,
                                    or playing videogames. You're in charge.</li>

                                    <li><b>Taste-setter </b>— you can influence your kids’ appreciation for the flavors
                                    of healthy food very early on, which can last them into adulthood.
                                    Start them off right.</li>

                                    <li><b>Advocate</b> — use your voice to push for positive changes in child care,
                                    schools and your communities that facilitate healthy eating and exercise.
                                    They have the right to be healthy.</li>
                                </ol>
                                
                            <h3>Change their diet </h3>
                                <ol type="1">
                                    <li>Use healthy, wholesome foods (i.e., fruits, vegetables, whole grains,
                                lean proteins, and low-fat dairy)
                                that are nutrient-dense when cooking family meals or preparing snacks.
                                Limit consumption of empty calories in the form of sugary, processed, and fast foods.
                            </li>

                                    <li>Use Go, Slow, Whoa as a guide.
                                    	<ol type="i">
                                    <li>Go foods — low in fat, sugar and calories and nutrient-dense, e.g., fruits and vegetables.
                                            Great to eat anytime.</li>
                                    <li>Slow foods — higher in fat, added sugar and calories,
                                            e.g. white bread, pancakes, fruit canned in syrup.
                                            Should be eaten less often.</li>
                                    
                                    <li>Whoa foods — very high in fat, added sugar and calories while low in nutrients,
                                            e.g., candy, soda, French fries.
                                            Eat once in a while in small portions.</li>
                                        </ol>
                                        </li>

                                   <li>Make sure that calorie intake and portion sizes are matched to your child’s age and activity level.</li>

                                    <li>Find ways to make over family favorites by using healthier ingredients
                                       (e.g., more veggies, less fat) or healthier methods of cooking
                                       (e.g., grilling or baking instead of frying).</li>

                                    <li>Connect kids to their food by continually introducing
                                   them to new types of healthy food and allowing them to
                                   participate in food shopping and preparation so that they can understand
                                   what goes into their meals and maintain healthy cooking habits as they grow up.</li>

                                    <li>Protect the time you eat with your kids — children who dine at regular family
                                   mealtimes get better nutrition, perform better in school and bond better with their families.</li>

                                    <li>Change their media diet — limit TV time to one to two hours of quality programming per day,
                                   monitor internet use and make sure they avoid snacking during TV time.</li>
                         </ol>


