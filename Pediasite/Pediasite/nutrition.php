		                   <h1>Introduction</h1><br>
                    <p>
                          Nutrition is the science that interprets the interaction of nutrients and
                          other substances in food in relation to maintenance, growth,
                          reproduction, health and disease of an organism. It includes food intake,
                          absorption, assimilation, biosynthesis,
                          catabolism and excretion.
                          The diet of an organism is what it eats, which is largely
                          determined by the availability and palatability of foods.
                          For humans, a healthy diet includes preparation of food and storage methods
                          that preserve nutrients from oxidation,
                          heat or leaching, and that reduce risk of foodborne illness.
                          In humans, an unhealthy diet can cause deficiency-related
                          diseases such as blindness, anemia, scurvy,
                          preterm birth, stillbirth and cretinism, or nutrient excess
                          health-threatening conditions such as obesity and metabolic syndrome;
                          and such common chronic systemic diseases as cardiovascular disease, diabetes,
                          and osteoporosis. Undernutrition can lead to wasting in acute cases,
                          and the stunting of marasmus in chronic cases of malnutrition.
                          Every child needs to learn the ABCs of life. When it comes to establishing healthy habits,
                          you can teach them how to “ABCDE” (Act Boldly to Change Diet and Exercise).
                    </p>
