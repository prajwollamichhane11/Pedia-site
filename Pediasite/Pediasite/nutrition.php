                    <h2> Why is it important? </h2>
                    <p>
                          Good nutrition and plenty of exercise are the building blocks for strong growth, healthy
                          development and lifelong wellbeing for children.
                          These days, too many children are not receiving the proper nutrition or enough exercise:
                          They are not eating enough — an estimated 16 to 17 million children live in homes where they are at risk of going hungry
                          (approximately one in six households).
                          They are not eating enough healthy food — an estimated one in three children are overweight and about one in six (ages six to 17) are obese.
                          They are not getting enough exercise — only 30 percent of children (aged six to 17) participated in
                          20 minutes plus of vigorous physical activity on a daily basis.
                          Children need to get 60 minutes of exercise on a daily basis.
                    </p>
                         <h3> Benefits of good nutrition</h3>
                         <p>
                           <ul><li><b>Mental and behavioral benefits </b></ul>
                           <p>
Good nutrition is essential to healthy brain development in children which is, of course, critical to learning.

Children who exercise regularly and eat healthily are likely to:

perform better academically feel better about themselves, their bodies and their abilities cope with stress and regulate their emotions better avoid feelings of low self-esteem, anxiety, and depression.
Establishing healthy eating and exercise habits early in life can lead to long term healthy behavior in adulthood.
                         </p>
<ul><li><b>Physical benefits </b></ul>
       <p>
Children need a wide variety of nutrients (e.g., protein, complex carbohydrates, healthy fats, minerals and vitamins) to assist in their daily growth and development and to protect them from childhood illnesses.
Daily exercise also helps children to build stronger muscles and bones and limit excess body fat. Healthy eating also cuts down on risk for cavities, eating disorders and unhealthy weight control behaviors
(i.e., fasting, skipping meals, eating very little food, vomiting, using diet pills, laxatives or diuretics), malnutrition and iron deficiency.
Healthy eating and consistent physical activity help to prevent chronic illnesses that appear in adulthood associated with obesity, e.g., heart disease, diabetes, high blood pressure and several forms of cancer.
                         </p>







